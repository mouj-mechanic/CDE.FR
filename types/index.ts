export type FingerId = "index" | "middle" | "ring" | "pinky";
export type HandJewelryType = "ring" | "bracelet";

export interface TryOnRequest {
  category: CategoryId;
  userImage: File;
  productImages: File[];
  productUrls: string[];
  /**
   * Optional, server-resolved transparent product cutouts (PNG with
   * alpha). The /api/try-on route downloads cutout URLs so providers
   * can use them as high-fidelity references without leaking URLs.
   */
  productCutoutBuffers?: Buffer[];
  notes?: string;
  merchantId?: string;
  /** Optional deterministic preview produced client-side. */
  previewImage?: File;
  /** Hand-jewelry subtype (ring | bracelet). */
  handJewelryType?: HandJewelryType;
  /** Target finger for rings. */
  ringFinger?: FingerId;
  /** Render mode requested by the client. */
  renderModeRequest?: "fast" | "premium" | "auto";
  /**
   * Inpainting refinement pack. When both fields are provided, the
   * service routes to a mask-aware model:
   *  - openai → gpt-image-1 image edit with alpha mask.
   *  - fal    → FLUX Fill / FLUX LoRA Inpainting.
   * Unmasked pixels are preserved exactly — protecting product details
   * and hand/face anatomy.
   */
  inpaintComposite?: File;
  inpaintMask?: File;
}

export interface TryOnResponseDebug {
  imageCount: number;
  productImageCount: number;
  productWasCutout?: boolean;
  productImageSource?: "transparent-upload" | "cutout" | "original";
  productHasAlpha?: boolean;
  productMimeType?: string;
  /** True when the request was handled by the OpenAI provider. */
  usedOpenAI?: boolean;
  /** True when the request was handled by a fal.ai model. */
  usedFal?: boolean;
  /**
   * True when the API returned a locally rendered image (fast-overlay /
   * canvas) as the *final* result. In API-only mode this must always be
   * false.
   */
  usedLocalRenderer?: boolean;
  /** Whether an OpenAI alpha mask was attached to the edit call. */
  maskUsed?: boolean;
  /** Whether the product-lock pipeline successfully re-stamped the product. */
  productLocked?: boolean;
  /** True when both composite + mask were generated by the app (no client mask). */
  autoMaskGenerated?: boolean;
  /** True when a composite was used as the base image sent to OpenAI. */
  compositeUsed?: boolean;
  /** True when the eligibility criteria for the product-lock pipeline were met. */
  productLockCandidate?: boolean;
  /** True when the product-lock re-stamp was actually applied. */
  productLockApplied?: boolean;
  /** True when the deterministic composite was returned as the final result. */
  fallbackUsed?: boolean;
  /** Width × height (px) of the canonical input image after resizing. */
  inputDimensions?: { width: number; height: number };
  /** Ratio of bright pixels in the mask sent to OpenAI (0..1). */
  maskCoverage?: number;
  /** True when the product reference had a usable alpha channel. */
  productAlphaDetected?: boolean;
  /** True when the post-generation product fidelity check flagged the result. */
  qualityCheckFailed?: boolean;
}

/** Lightweight server-side fidelity checks computed after generation. */
export interface QualityChecks {
  /** 0..1 score, higher is more preserved (outside-mask area). */
  outsideMaskChangeScore: number;
  /** True when the score >= preservation threshold (default 0.92). */
  outsideMaskPreserved: boolean;
  /** True when the product reference is below the resolution threshold. */
  productFidelityWarning: boolean;
  /** Aggregate flag: anything that should make the operator double-check. */
  customerPreservationWarning: boolean;
  /**
   * True when the original product PNG was re-stamped on top of the AI
   * result (product-lock pipeline). Always false for clothes.
   */
  productLocked?: boolean;
  /** How product fidelity was enforced for this generation. */
  productFidelityMode?:
    | "ai-only"
    | "locked-overlay-after-ai"
    | "locked-overlay-skipped";
  /**
   * Ratio of pixels considered "product silhouette" when running the
   * lock pipeline. Useful for debugging. 0..1.
   */
  productSilhouetteRatio?: number;
}

export type RenderMode =
  | "fast-overlay"
  | "premium-ai"
